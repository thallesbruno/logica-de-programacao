# in, not in
#  verificam se um objeto está associado ou
#  pertence a determinada estrutura de dados
lista = [1, 2, 3, 5]

# in --> True
# not in --> False
# comparação entre objetos

existe4 = 4 in lista # False
existe5 = 5 in lista # True
print(existe4)
print(existe5)

naoExiste1 = 1 not in lista # False
naoExiste4 = 4 not in lista # True
print(naoExiste1)
print(naoExiste4)